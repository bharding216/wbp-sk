                <h3>Port Authority of New York &amp; New Jersey</h3>
                <p>Program consisted of identifying, reviewing, assessing and scoping projects for potential federal disaster recovery funding. The program utilized a multi-disciplinary team who evaluated and reviewed the five business units of the PANYNJ that include tunnels, terminals, airports, trains and the world trade center.&nbsp;<strong>This effort resulted in a report of potential funding and financing for needed infrastructure repairs and improvements that could potentially increase the available funding for the PANYNJ from an estimated $2.5 billion to over $5 billion.</strong> The agency was led through a process of perspective change to understand how funding could be accessed and applied. The efforts of the program were instrumental in shifting the operational elements perspective to opportunistically pursuing potential funding rather than rigid acceptance of perceived requirements.</p>
            
                <h3>Alamo Area Development Corporation (AADC)</h3>
                <p>Program consisted of analyzing, assessing and developing strategies to address the increasing demands on the 71 county regional infrastructure. Issues addressed Included municipal planning, public health and safety, education, workforce and economic development associated with the Eagle Ford Shale oil and gas boom.</p>

                <h3>City of Cotulla</h3>
                <p>To address the increasing demands on the City of Cotulla&rsquo;s infrastructure and other issues such as municipal planning, public health and safety, education, workforce and economic develop, this program required a team to analyze, access and develop strategies that the City could implement.</p>
                <p>Tom Wendorf, with direction from the City Manager, led the Mayor and City Council in an effort to understand how their vision for their community could be executed in concert with the current oil and gas boom. The forum guided the city through an open process, rich with community dialogue and stakeholder leadership. The best interests of the residents of Cotulla were always in the forefront of the work to balance economic growth with planned management techniques. <strong>As a result, Cotulla developed a robust, funded Capital Improvements Program and a Hospital District that will deliver a regional hospital and other community medical facilities.</strong><br><small></small></p>

                <h3>Texas Office of Rural Community Affairs (ORCA)</h3>
                <p>This Program consisted of providing technical support to 167 non entitlement communities in a 29 county area of Texas hardest hit by Hurricane Ike. The goal: to get infrastructure projects for recovery from Hurricane Ike identified, assessed, scoped and estimated as rapidly as possible to accelerate recovery. Due to the unprecedented nature and extent of the damage, ORCA required a new approach to identify and quantify the infrastructure need and recovery requirements. The program provided engineering expertise, public involvement services, preliminary environmental assessment, technology, alternative delivery recommendations, planning, and program management services. This <strong>effort put TxORCA 3 months ahead of the previous Hurricane Rita effort that was 10 times smaller than the Ike effort. Federal funding was increased from $1.4 billion to $3.2 billion</strong></p>
