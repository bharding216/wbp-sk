                <p>Wendorf Beward &amp; Partners are excited to work with clients as they continue to refine and expand management of one of their critical resources. Other than personnel, entity assets are a key component to providing services that support their mission. While many agencies have developed condition assessments of the majority of their assets, the critical step missing is the implementation of a Strategic Asset Management Plan.</p>
                <p>A&nbsp;<strong>Strategic Asset Management Plan</strong> demonstrates the stewardship of an agency&rsquo;s highest profile assets, demonstrates fiscal discipline in expenditures, establishes a defined level of maintenance and provides for transparency and accountability of its assets to the entities stakeholders through the operational and capital budget.</p>
                <p>A Strategic Asset Management Plan also demonstrates knowledge of assets that communicates to leadership that asset management is occurring with regard to inventory, condition, supporting the entities strategy, goals and objectives and is backed up through budget development and execution. Agencies benefit from a Strategic Asset Management Program by providing:</p>
                <ul>
                    <li>Organizational understanding that assets are a key component of service delivery</li>
                    <li>Organizational goals and policies are developed and understood across the organization</li>
                    <li>Business processes are integrated across the organization, reducing the &ldquo;silo&rdquo; affect</li>
                    <li>Asset structure is established across the entire organization</li>
                    <li>Risks are better understood</li>
                    <li>Asset maintenance and procurement are linked through the operational and capital budgets</li>
                </ul>
